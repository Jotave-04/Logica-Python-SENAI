# OS 7 OPERADORES ESSENCIAIS

print(5 + 2) # SOMA
print(5 - 2) # SUBTRAÇÃO
print(5 * 2) # MULTIPLICAÇÃO
print(5 / 2) # DIVISÃO
print(5 ** 2) # POTÊNCIA
print(5 // 2) # DIVISÃO INTEIRA
print(5 % 2) # MÓDULO


# STRINGS TAMBÉM SÃO OPERÁVEIS

print("Oi" + " Olá") # Oi Olá  | Concatenação com +
print("Oi" * 5) # OiOiOiOiOi  | Replicação com *
print("=" * 20) # ====================  | Replicação com *